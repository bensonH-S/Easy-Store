    public void preencherComboBox(){
        
        jComboBoxCidade.removeAllItems();
        conectaClientes.conexao();
        
        conectaClientes.executaSQL("SELECT * FROM cidade ORDER BY nome_cidade");
        
            try {
                conectaClientes.rs.first();
                do {
                    
                    jComboBoxCidade.addItem(conectaClientes.rs.getString("nome_cidade"));
                } while (conectaClientes.rs.next());
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ERRO AO CONECTAR"+ ex);
            }finally{
                
                conectaClientes.desconecta();
            }
            
            
        
    }
    
